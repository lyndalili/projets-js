"use strict";

// Dans cette variable se trouve la date actuelle
let dateActuelle = new Date();

// Récupère dans la variable `jour` le jour de la `dateActuelle`
let jour=dateActuelle.getDay();

// Vérifie dans la console que la variable `jour` contient bien le jour de la `dateActuelle`
console.log(dateActuelle);
console.log(jour);

